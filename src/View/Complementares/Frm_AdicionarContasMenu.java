    public void PreencherComboFornecedor (){
        
        Conexao.conexao();
        
        Conexao.executaSQL("select * from fornecedor order by nome_fornecedor");
         
        try {
            Conexao.rs.first();
            
            jComboBoxfornecedor.removeAllItems();
            jComboBoxfornecedor.addItem("-Sel-");
            do{
                 
             jComboBoxfornecedor.addItem(Conexao.rs.getString("nome_fornecedor"));
             }while (Conexao.rs.next());
             
        } catch (SQLException ex) {
            //Logger.getLogger(JI_Pecas.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

    
    
