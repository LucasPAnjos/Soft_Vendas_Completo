    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String VL_Total = jFormattedTextFieldVL_Total.getText();
        Float VL_TotalFloat;
        
            
        if(jFormattedTextFieldCód.getText().equals("")){
            
            JOptionPane.showMessageDialog(null, "Código Não Pode ser Nulo!");
            
        }else{
        
            if(jFormattedTextFieldDescricao.getText().equals("")){
                
                JOptionPane.showMessageDialog(null, "Descrição Não Pode ser Nulo!");
                
            }else{
            
            if(jFormattedTextFieldVL_Total.getText().equals("")){
                
                JOptionPane.showMessageDialog(null, "Valor Não Pode ser Nulo!");
                
            }else{
                
                
                if(jFormattedTextFieldjTextFieldQuantidade_Adicionar.getText().equals("")){
                  
                    JOptionPane.showMessageDialog(null, "Quantidade Não Pode ser Nulo!");
                    
                }else{
       
                //Dados NF
        ModNF.setNumero_NF(jFormattedTextFieldNumero_NF.getText());
        ModNF.setIcms(Float.parseFloat("0"));
        ModNF.setIpi(Float.parseFloat("0"));
        ModNF.setValor_total_NF(Float.parseFloat(jFormattedTextFieldVL_Total.getText()));
        
        //Emissão NF Data Para Sql
        String dia = jFormattedTextFieldData_Emissao.getText().substring(0,2);
        String mes = jFormattedTextFieldData_Emissao.getText().substring(3,5);
        String ano = jFormattedTextFieldData_Emissao.getText().substring(6);
        ModNF.setData_emissao_NF(ano+"-"+mes+"-"+dia);

        //Vencimento NF Data Para Sql
        String diav = jFormattedTextFieldData_Vencimento.getText().substring(0,2);
        String mesv = jFormattedTextFieldData_Vencimento.getText().substring(3,5);
        String anov = jFormattedTextFieldData_Vencimento.getText().substring(6);
        ModNF.setData_vencimento_NF(anov+"-"+mesv+"-"+diav);

        ModNF.setQuantidade_NF(Float.parseFloat(jFormattedTextFieldjTextFieldQuantidade_Adicionar.getText()));

        ModNF.setSituacao("Em Aberto");
        
        ModNF.setId_Prod(Integer.parseInt(jFormattedTextFieldCód.getText()));
 
        ModNF.setFornecedor((String) jComboBoxfornecedor.getSelectedItem());
        
        AdicionarContasBO.Adicionar_Contas(ModNF);
        
           }
          }
         }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButtonPesquisarCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarCategoriaActionPerformed
        // TODO add your handling code here:
        
        Conexao.conexao();
        
        Conexao.executaSQL("SELECT * FROM pecas where `idPecas` ='"+jFormattedTextFieldCód.getText()+"'");
        try {
            Conexao.rs.first();
            
            int Cod_Categoria = Conexao.rs.getInt("idPecas");
            String Descricao_Categoria = Conexao.rs.getString("nome_peca");
            jFormattedTextFieldCód.setText(String.valueOf(Cod_Categoria));
            jFormattedTextFieldDescricao.setText(String.valueOf(Descricao_Categoria));
            
        } catch (SQLException ex) {
        
            JOptionPane.showMessageDialog(null, "Erro de Busca! Verifique o Código");
        
        }
        
    }//GEN-LAST:event_jButtonPesquisarCategoriaActionPerformed

