    public void PreencherTabela_Estado (String sql){
        
        Conexao.conexao();
        ArrayList dados = new ArrayList();
        
        String[] Colunas = new String[]{"Código", "Nome Estado"};
    
    
    Conexao.executaSQL(sql);
    
        try {
            Conexao.rs.first();
            
            do{
                dados.add(new Object [] {Conexao.rs.getString("idSetor"),Conexao.rs.getString("Nome_Setor")});
            }while (Conexao.rs.next()); 
                          
            } catch (Exception ex) {
             JOptionPane.showMessageDialog(null, "Erro no preenchimento do Array List "+ex);
        }
                
        ModeloTabela modelo = new ModeloTabela(dados,Colunas);
       
        
        jTableCombustivel.setModel(modelo);
        
        jTableCombustivel.getColumnModel().getColumn(0).setPreferredWidth(100);
        jTableCombustivel.getColumnModel().getColumn(0).setResizable(false);
        jTableCombustivel.getColumnModel().getColumn(1).setPreferredWidth(650);
        jTableCombustivel.getColumnModel().getColumn(1).setResizable(false);
        
        jTableCombustivel.setAutoResizeMode(jTableCombustivel.AUTO_RESIZE_OFF);
        jTableCombustivel.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        Conexao.desconecta();
    
        
    }
   
    
    
