    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        // TODO add your handling code here:

        if(jTextFieldCodigo.getText().equals("")){

            ModFuncionario.setNome_Setor(jTextFieldsetor.getText());

            PreencherTabela_Estado("select * from setor where Nome_Setor like'%"+ModFuncionario.getNome_Setor()+"%'");

            ControlFuncionario.Consultar_NomeSetor(ModFuncionario);

            jTextFieldCodigo.setText(String.valueOf(ModFuncionario.getIdSetor()));
            jTextFieldsetor.setText(String.valueOf(ModFuncionario.getNome_Setor()));

        }else

        if(jTextFieldsetor.getText().equals("")){

            ModFuncionario.setIdSetor(Integer.parseInt(jTextFieldCodigo.getText()));

            PreencherTabela_Estado("select * from setor where idSetor ='"+ModFuncionario.getIdSetor()+"'");

            ControlFuncionario.Consultar_Setor(ModFuncionario);

            jTextFieldCodigo.setText(String.valueOf(ModFuncionario.getIdSetor()));
            jTextFieldsetor.setText(String.valueOf(ModFuncionario.getNome_Setor()));

        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

