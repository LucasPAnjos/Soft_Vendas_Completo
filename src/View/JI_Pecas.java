    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        // TODO add your handling code here:

String id;

id = jTextFieldCodigo.getText();

        if(jTextFieldCodigo.getText().equals("")){

            ModPecas.setPesquisaNomePeca(jTextFieldNomePeca.getText());

            ControlPecas.Pesquisar_NomePecas(ModPecas);

            jTextFieldCodigo.setText(String.valueOf(ModPecas.getId_pecas()));
            jTextFieldNomePeca.setText(String.valueOf(ModPecas.getNome_pecas()));
            jTextFieldFabricante.setText(String.valueOf(ModPecas.getFabricante()));
            jTextFieldCod_Barras.setText(String.valueOf(ModPecas.getCod_Barras()));
            PreencherTabelaPecas("select * from pecas where nome_peca like '%"+ModPecas.getPesquisaNomePeca()+"%'");

                                //Ativar Botões
        jButtonAtualizar_Registro.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        jButtonAnterior.setEnabled(true);
        jButtonProximo.setEnabled(true);

            
        }else

        if (id != null){

            ModPecas.setPesquisaIDPecas(Integer.parseInt(jTextFieldCodigo.getText()));

            ControlPecas.ConsultarPecasID(ModPecas);

            jTextFieldCodigo.setText(String.valueOf(ModPecas.getId_pecas()));
            jTextFieldNomePeca.setText(String.valueOf(ModPecas.getNome_pecas()));
            jTextFieldFabricante.setText(String.valueOf(ModPecas.getFabricante()));
            jTextFieldCod_Barras.setText(String.valueOf(ModPecas.getCod_Barras()));

            
            PreencherTabelaPecas("select * from pecas where idPecas ='"+ModPecas.getPesquisaIDPecas()+"'");

                                //Ativar Botões
        jButtonAtualizar_Registro.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        jButtonAnterior.setEnabled(true);
        jButtonProximo.setEnabled(true);

            
        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

