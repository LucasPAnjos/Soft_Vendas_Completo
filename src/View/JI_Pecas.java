    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:

               try {
           
        Conexao.conexao();

        Conexao.executaSQL("select * from pecas where idPecas ='"+jTextFieldCodigo.getText()+"'");
        
       
           Conexao.rs.first();
        
           String Cod_Pecas = Conexao.rs.getString("idPecas");
           
           if(Cod_Pecas != ""){

        
        Conexao.conexao();
        
                if(JOptionPane.showConfirmDialog(null, "Confirmar Exclusão do registro", "Pergunta", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE)== JOptionPane.YES_NO_OPTION){

        
        Conexao.executaSQLupdates("delete from pecas where idPecas='"+jTextFieldCodigo.getText()+"'");

                }
           }
                       } catch (SQLException ex) {
           
           JOptionPane.showMessageDialog(null, "Registro Não Localizado!");
           
           Conexao.desconecta();
       }

                
    }//GEN-LAST:event_jButtonExcluirActionPerformed

