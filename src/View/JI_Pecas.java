    public void PreencherTabelaPecas (String sql){
        
        Conexao.conexao();
        ArrayList dados = new ArrayList();
        
        String[] Colunas = new String[]{"Código", "Descrição", "Cod. de Barras","Fabricante"};
    
    
    Conexao.executaSQL(sql);
    
        try {
            Conexao.rs.first();
            
            do{
                dados.add(new Object [] {Conexao.rs.getString("idPecas"),Conexao.rs.getString("nome_peca"),Conexao.rs.getString("Cod_Barras"),Conexao.rs.getString("fabricante_pecas_idfabricante_pecas")});
            }while (Conexao.rs.next()); 
                          
            } catch (Exception ex) {
            // JOptionPane.showMessageDialog(null, "Erro no preenchimento do Array List "+ex);
        }
                
        ModeloTabela modelo = new ModeloTabela(dados,Colunas);
       
        
        jTableListarPecas.setModel(modelo);
        
        jTableListarPecas.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTableListarPecas.getColumnModel().getColumn(0).setResizable(false);
        jTableListarPecas.getColumnModel().getColumn(1).setPreferredWidth(450);
        jTableListarPecas.getColumnModel().getColumn(1).setResizable(false);
        jTableListarPecas.getColumnModel().getColumn(2).setPreferredWidth(170);
        jTableListarPecas.getColumnModel().getColumn(2).setResizable(false);
        jTableListarPecas.getColumnModel().getColumn(3).setPreferredWidth(170);
        jTableListarPecas.getColumnModel().getColumn(3).setResizable(false);
        
        jTableListarPecas.setAutoResizeMode(jTableListarPecas.AUTO_RESIZE_OFF);
        jTableListarPecas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        Conexao.desconecta();
        
    }


            public void Valores_TabelaPesquisa (){
          
          int Cod_Tabela;         
          int Selecao = jTableListarPecas.getSelectedRow();
          Cod_Tabela = Integer.parseInt(jTableListarPecas.getModel().getValueAt(Selecao,0).toString());
         // jTextFieldplaca.setText(jTabletabelaItensVenda.getModel().getValueAt(Selecao, ));
          ModPecas.setId_pecas(Cod_Tabela);
   
        
        jTextFieldCodigo.setText(String.valueOf(ModPecas.getId_pecas()));   
         
    }

    
