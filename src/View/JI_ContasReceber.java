    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        // TODO add your handling code here:

        
        if(jComboBoxFiltro_Pesquisa.getSelectedItem().equals("-Sel-")){
        
        PreencherTabela_Cargo("SELECT * FROM venda_pecas inner join cadastro_cliente on venda_pecas.`Cadastro_cliente_idCadastro_cliente` = cadastro_cliente.`idCadastro_cliente` WHERE venda_pecas.`Forma_Pagamento`=\"A Prazo\" ");

        }else{
            
            if(jComboBoxFiltro_Pesquisa.getSelectedItem().equals("Código do Cliente")){
                
                if(jTextFieldID_Cliente.getText().equals("")){
                
                    JOptionPane.showMessageDialog(null, "Infome o Código do Cliente!");
                    
                }else{
                
                PreencherTabela_Cargo("SELECT * FROM venda_pecas inner join cadastro_cliente on venda_pecas.`Cadastro_cliente_idCadastro_cliente` = cadastro_cliente.`idCadastro_cliente` WHERE venda_pecas.`Forma_Pagamento`=\"A Prazo\" and cadastro_cliente.`idCadastro_cliente`='"+jTextFieldID_Cliente.getText()+"'");
                }
                
            }else{
                
                if(jComboBoxFiltro_Pesquisa.getSelectedItem().equals("Código da Venda")){
                    
                    if(jTextFieldCod_Venda.getText().equals("")){
                     
                        JOptionPane.showMessageDialog(null, "Infome o Código da Venda!");
                        
                    }else{
                    
                    PreencherTabela_Cargo("SELECT * FROM venda_pecas inner join cadastro_cliente on venda_pecas.`Cadastro_cliente_idCadastro_cliente` = cadastro_cliente.`idCadastro_cliente` WHERE venda_pecas.`Forma_Pagamento`=\"A Prazo\" and venda_pecas.`idVenda_pecas`='"+jTextFieldCod_Venda.getText()+"' ");
                    
                    }
                    
                }else{
                    
                    if(jComboBoxFiltro_Pesquisa.getSelectedItem().equals("Número do RG")){
                        
                        if(jTextFieldRG_Cliente.getText().trim().length() != 11){
                        
                            JOptionPane.showMessageDialog(null, "Infome Número do RG do Cliente!");
                            
                        }else{
                        
                        PreencherTabela_Cargo("SELECT * FROM venda_pecas inner join cadastro_cliente on venda_pecas.`Cadastro_cliente_idCadastro_cliente` = cadastro_cliente.`idCadastro_cliente` WHERE venda_pecas.`Forma_Pagamento`=\"A Prazo\" and cadastro_cliente.rg='"+jTextFieldRG_Cliente.getText()+"'");
                        
                        }
                        
                    }else{
                        
                        if(jComboBoxFiltro_Pesquisa.getSelectedItem().equals("Número de CPF")){
                            
                            if(jTextFieldCPF_Cliente.getText().trim().length() != 14){
                             
                                JOptionPane.showMessageDialog(null, "Infome Número do CPF do Cliente!");
                                
                            }else{
                            
                            PreencherTabela_Cargo("SELECT * FROM venda_pecas inner join cadastro_cliente on venda_pecas.`Cadastro_cliente_idCadastro_cliente` = cadastro_cliente.`idCadastro_cliente` WHERE venda_pecas.`Forma_Pagamento`=\"A Prazo\" and cadastro_cliente.cpf='"+jTextFieldCPF_Cliente.getText()+"' ");
                            
                            }
                            
                        }else{
                            
                            if(jComboBoxFiltro_Pesquisa.getSelectedItem().equals("Data de Vencimento")){
                                
                            //Data Validade
                            String dia = jFormattedTextFieldData_Vencimento.getText().substring(0,2);
                            String mes = jFormattedTextFieldData_Vencimento.getText().substring(3,5);
                            String ano = jFormattedTextFieldData_Vencimento.getText().substring(6);
                                
                            String Data_Vencimento = ano+"-"+mes+"-"+dia;
                            
                            
                                PreencherTabela_Cargo("SELECT * FROM venda_pecas inner join cadastro_cliente on venda_pecas.`Cadastro_cliente_idCadastro_cliente` = cadastro_cliente.`idCadastro_cliente` WHERE venda_pecas.`Forma_Pagamento`=\"A Prazo\" and venda_pecas.`Vencimento1` >='"+Data_Vencimento+"'");
                                
                            }
                            
                        }
                        
                    }
                    
                }
                
            }
            
        }
        
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

