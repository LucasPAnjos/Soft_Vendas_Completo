                dados.add(new Object [] {Conexao.rs.getInt("idVenda_pecas"),Conexao.rs.getString("nome_cliente"),Conexao.rs.getString("cpf"),Conexao.rs.getString("data_venda"),Conexao.rs.getString("Quant_Parcelas"),Conexao.rs.getString("Pag_vencimento1"),Conexao.rs.getString("Pag_vencimento2"),Conexao.rs.getString("Pag_vencimento3")});
