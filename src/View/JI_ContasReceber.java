    private void jTextFieldDescontoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTextFieldDescontoMouseClicked
        // TODO add your handling code here:
        
                
   String Margem_AddString = jTextFieldDesconto.getText();
   Float Margem_AddFloat;      

   NumberFormat DinheiroUS = NumberFormat.getNumberInstance(localeUS);
   DinheiroUS.setMaximumFractionDigits(2);

   
   try {
       
       Margem_AddFloat = Float.parseFloat(Margem_AddString);
   
       if(jTextFieldDesconto.getText().equals("")){
            
            jTextFieldDesconto.setText("0.0");
            
        }else{
        
            float VLDesconto = Float.parseFloat(jTextFieldDesconto.getText());
            
            if(VLDesconto >= 0){
            
                
                //Calcular 
                Float Valor_Pagar = Float.parseFloat(jTextFieldvalorparcelas2.getText());
                Float Juros = Float.parseFloat(jTextFieldDesconto.getText()), ResJuros, ResTotal, ResParcela;
                
                //Juros
                ResJuros = (Juros/100)*Valor_Pagar;
                
                //Total
                ResTotal = Valor_Pagar-ResJuros;

                jTextFieldTotal_Pagamento.setText(String.valueOf(DinheiroUS.format(ResTotal)));

                
                
            }
        }
       
   }catch (NumberFormatException ex){

       jTextFieldDesconto.setText("0.0");
       
   }
                
        
    }//GEN-LAST:event_jTextFieldDescontoMouseClicked

