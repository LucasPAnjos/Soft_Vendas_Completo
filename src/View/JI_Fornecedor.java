    public void PreencherTabelaProduto (String slq){
        
        //Conecta_Banco Conexao = new Conecta_Banco();
    
        Conexao.conexao();
        ArrayList dados = new ArrayList();
        
        String[] Colunas = new String[]{"C√≥digo", "Nome", "CNPJ","Telefone"};
    
    
    Conexao.executaSQL(slq);
    
        try {
            Conexao.rs.first();
            
            do{
                dados.add(new Object [] {Conexao.rs.getString("idfornecedor"),Conexao.rs.getString("nome_fornecedor"),Conexao.rs.getString("cnpj_fornecedor"),Conexao.rs.getString("telefone_fornecedor")});
            }while (Conexao.rs.next()); 
                          
            } catch (Exception ex) {
            // JOptionPane.showMessageDialog(null, "Erro no preenchimento do Array List "+ex);
        }
                
        ModeloTabela modelo = new ModeloTabela(dados,Colunas);
       
        
        jTableDadosFornecedor.setModel(modelo);
        
        jTableDadosFornecedor.getColumnModel().getColumn(0).setPreferredWidth(100);
        jTableDadosFornecedor.getColumnModel().getColumn(0).setResizable(false);
        jTableDadosFornecedor.getColumnModel().getColumn(1).setPreferredWidth(380);
        jTableDadosFornecedor.getColumnModel().getColumn(1).setResizable(false);
        jTableDadosFornecedor.getColumnModel().getColumn(2).setPreferredWidth(190);
        jTableDadosFornecedor.getColumnModel().getColumn(2).setResizable(false);
        jTableDadosFornecedor.getColumnModel().getColumn(3).setPreferredWidth(150);
        jTableDadosFornecedor.getColumnModel().getColumn(3).setResizable(false);
        jTableDadosFornecedor.getTableHeader().setReorderingAllowed(false);
        jTableDadosFornecedor.setAutoResizeMode(jTableDadosFornecedor.AUTO_RESIZE_OFF);
        jTableDadosFornecedor.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        Conexao.desconecta();
        
    }
    
    
