        public void PreencherTabelaProduto (String slq){
        
        //Conecta_Banco Conexao = new Conecta_Banco();
    
        Conexao.conexao();
        ArrayList dados = new ArrayList();
        
        String[] Colunas = new String[]{"C√≥digo", "Nome", "CPF","Telefone","Cargo","Setor"};
    
    
    Conexao.executaSQL(slq);
    
        try {
            Conexao.rs.first();
            
            do{
                dados.add(new Object [] {Conexao.rs.getString("idFuncionario"),Conexao.rs.getString("Nome_Funcionario"),Conexao.rs.getString("CPF_Funcionario"),Conexao.rs.getString("Telefone_Funcionario"),Conexao.rs.getString("Nome_Cargo"),Conexao.rs.getString("Nome_Setor")});
            }while (Conexao.rs.next()); 
                          
            } catch (Exception ex) {
            // JOptionPane.showMessageDialog(null, "Erro no preenchimento do Array List "+ex);
        }
                
        ModeloTabela modelo = new ModeloTabela(dados,Colunas);
       
        
        jTableDadosFornecedor.setModel(modelo);
        
        jTableDadosFornecedor.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTableDadosFornecedor.getColumnModel().getColumn(0).setResizable(false);
        jTableDadosFornecedor.getColumnModel().getColumn(1).setPreferredWidth(262);
        jTableDadosFornecedor.getColumnModel().getColumn(1).setResizable(false);
        jTableDadosFornecedor.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTableDadosFornecedor.getColumnModel().getColumn(2).setResizable(false);
        jTableDadosFornecedor.getColumnModel().getColumn(3).setPreferredWidth(90);
        jTableDadosFornecedor.getColumnModel().getColumn(3).setResizable(false);
        jTableDadosFornecedor.getColumnModel().getColumn(4).setPreferredWidth(135);
        jTableDadosFornecedor.getColumnModel().getColumn(4).setResizable(false);
        jTableDadosFornecedor.getColumnModel().getColumn(5).setPreferredWidth(135);
        jTableDadosFornecedor.getColumnModel().getColumn(5).setResizable(false);
        
        jTableDadosFornecedor.getTableHeader().setReorderingAllowed(false);
        
        jTableDadosFornecedor.setAutoResizeMode(jTableDadosFornecedor.AUTO_RESIZE_OFF);
        jTableDadosFornecedor.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        Conexao.desconecta();
        
    }
    
    public void PreencherComboEstado(){
        
        Conexao.conexao();
        
        Conexao.executaSQL("select * from estado order by nome_estado");
        
        try {
            Conexao.rs.first();
            
            jComboBoxestado.removeAllItems();
            jComboBoxestado.addItem(Dados_Inicio.getCombobox_selecionar());
            do{
                
                jComboBoxestado.addItem(Conexao.rs.getString("nome_estado"));
                
            }while(Conexao.rs.next());
            
        } catch (SQLException ex) {
          //  Logger.getLogger(JI_Funcionario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Conexao.desconecta();
        
    }
    
    
    
